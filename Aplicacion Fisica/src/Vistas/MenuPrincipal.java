    private void botonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCalcularActionPerformed
        // TODO add your handling code here:
        repaint();
        try {
            if(txtCarga1.getText().equals("")){
                carga2=Double.parseDouble(txtCarga2.getText());
                distancia=Double.parseDouble(txtDistancia.getText());
                fuerza=Double.parseDouble(txtFuerza.getText()); 
                txtCarga1.setText(Operaciones.calcularCarga(carga2,distancia,fuerza));
            }
            else if(txtCarga2.getText().equals("")){
                carga1=Double.parseDouble(txtCarga1.getText());
                distancia=Double.parseDouble(txtDistancia.getText());
                fuerza=Double.parseDouble(txtFuerza.getText()); 
                txtCarga2.setText(Operaciones.calcularCarga(carga1,distancia,fuerza));
            }
            else if(txtDistancia.getText().equals("")){
                carga1=Double.parseDouble(txtCarga1.getText());
                carga2=Double.parseDouble(txtCarga2.getText());
                fuerza=Double.parseDouble(txtFuerza.getText());
                txtDistancia.setText(Operaciones.calcularDistancia(carga1, carga2, fuerza));
            }
            else if(txtFuerza.getText().equals("")){
                carga1=Double.parseDouble(txtCarga1.getText());
                carga2=Double.parseDouble(txtCarga2.getText());
                distancia=Double.parseDouble(txtDistancia.getText());
                txtFuerza.setText(Operaciones.calcularFuerza(carga1, carga2, distancia));
            }
            else {
                carga1=Double.parseDouble(txtCarga1.getText());
                carga2=Double.parseDouble(txtCarga2.getText());
                distancia=Double.parseDouble(txtDistancia.getText());
                fuerza=Double.parseDouble(txtFuerza.getText());
                JOptionPane.showMessageDialog(null, "Deja una casilla vacia", "Error", WARNING_MESSAGE);
            }
        } 
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Verifica los valores ingresados", "ERROR", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonCalcularActionPerformed

