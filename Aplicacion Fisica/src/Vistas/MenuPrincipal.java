    private void BotonLimpiar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonLimpiar1ActionPerformed
        // TODO add your handling code here:
        Plano p = new Plano(this.panel);
        try {
            if((Double.parseDouble(txtCarga1.getText()))>0&&(Double.parseDouble(txtCarga2.getText()))>0){
                p.c1();
                pantallaLimpia=false;
            }
            else if((Double.parseDouble(txtCarga1.getText())<0)&&(Double.parseDouble(txtCarga2.getText())<0)){
                p.c2();
                pantallaLimpia=false;
            }
            else if((Double.parseDouble(txtCarga1.getText())<0)&&(Double.parseDouble(txtCarga2.getText())>0)){
                p.c3();
                pantallaLimpia=false;
            }
            else if((Double.parseDouble(txtCarga1.getText())>0)&&(Double.parseDouble(txtCarga2.getText())<0)){
                p.c4();
                pantallaLimpia=false;
            }
        } 
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Verifica los valores ingresados", "ERROR", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonLimpiar1ActionPerformed

